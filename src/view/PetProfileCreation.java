    private void SharebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SharebtnActionPerformed
try {
        String imageUrl = "https://imgur.com/a/n8A8JAT";  // Your image URL

        // New window (JFrame)
        JFrame imageFrame = new JFrame("Link Displayed");
        imageFrame.setSize(500, 500);
        imageFrame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        imageFrame.setLayout(new BorderLayout());

        // Image label (initially empty)
        JLabel imageLabel = new JLabel();
        imageLabel.setHorizontalAlignment(SwingConstants.CENTER);

        // Display the link as a JLabel with HTML (clickable style)
        JLabel linkLabel = new JLabel("<html><font color='blue'><u>" + imageUrl + "</u></font></html>");
        linkLabel.setCursor(new Cursor(Cursor.HAND_CURSOR));

        // When the link is clicked → show the image
        linkLabel.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                try {
                    URL url = new URL(imageUrl);
                    ImageIcon originalIcon = new ImageIcon(url);
                    
                    // Scale the image to fit
                    Image scaledImage = originalIcon.getImage().getScaledInstance(
                        400, 300, Image.SCALE_SMOOTH);
                    ImageIcon scaledIcon = new ImageIcon(scaledImage);

                    imageLabel.setIcon(scaledIcon);
                    imageFrame.revalidate(); // Refresh layout
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(imageFrame, "Could not load image.");
                }
            }
        });

        // Copy Link button
        JButton copyButton = new JButton("Copy Link");
        copyButton.addActionListener(e -> {
            StringSelection selection = new StringSelection(imageUrl);
            Clipboard clipboard = Toolkit.getDefaultToolkit().getSystemClipboard();
            clipboard.setContents(selection, null);
            JOptionPane.showMessageDialog(imageFrame, "Link copied to clipboard!");
        });

        // Top panel: link + button
        JPanel topPanel = new JPanel(new FlowLayout());
        topPanel.add(linkLabel);
        topPanel.add(copyButton);

        // Add components to window
        imageFrame.add(topPanel, BorderLayout.NORTH);
        imageFrame.add(imageLabel, BorderLayout.CENTER);

        imageFrame.setVisible(true);

    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error opening viewer window");
    }        // TODO add your handling code here:
    }//GEN-LAST:event_SharebtnActionPerformed

