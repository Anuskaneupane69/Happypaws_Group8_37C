    private void SaveAdoptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveAdoptActionPerformed
        System.out.println("Selected Adoption Image Path: " + adoptionImgPath);
    try {
        String name = this.Adoptname.getText();         // Get adopter's name
        String species = this.Adoptspecies.getText();   // Get adopted species
        int age = Integer.parseInt(this.Adoptage.getText()); // Get age
        String info = this.Adoptinfo.getText();         // Get additional adoption info
        String contactNo = this.Adoptno.getText();      // Get contact number
        // Create AdoptionModel instance with the image path
        AdoptionModel adoption = new AdoptionModel(name, species, age, info, contactNo, adoptionImgPath);
        
        // Save to the database using AdoptionDao
        AdoptionDao adoptionDao = new AdoptionDao();
        adoptionDao.createAdoption(adoption);
        
        System.out.println("Adoption saved: " + name);
    } catch (NumberFormatException ex) {
        System.out.println("Error: Please enter valid numbers for age.");
    } catch (Exception ex) {
        System.out.println("Error: " + ex.getMessage());
    }
    }//GEN-LAST:event_SaveAdoptActionPerformed

    private String adoptionImgPath;
    
    private void adoptionImgActionPerformed(java.awt.event.ActionEvent evt) {                                            
       JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Select Adoption Image");
        // Set file filter for image files
        fileChooser.setAcceptAllFileFilterUsed(false);
        fileChooser.addChoosableFileFilter(new javax.swing.filechooser.FileNameExtensionFilter(
                "Image files", "jpg", "png", "jpeg", "gif"));
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            adoptionImgPath = selectedFile.getAbsolutePath(); // Store the path here
            // Example: If you want to display the selected image in a JLabel
            ImageIcon icon = new ImageIcon(adoptionImgPath);
            Image resizedImage = icon.getImage().getScaledInstance(150, 100, Image.SCALE_SMOOTH);
            JLabel imageLabel = new JLabel(new ImageIcon(resizedImage));
            // You should have a panel to add this label to, e.g., imagePanel
            JPanel imagePanel = new JPanel(); // Assuming you have an imagePanel defined somewhere
            imagePanel.removeAll(); // Clear existing images if necessary
            imagePanel.add(imageLabel);
            imagePanel.revalidate();
            imagePanel.repaint();
        }
    }
    public String getAdoptionImgPath() {
        return adoptionImgPath; // Method to retrieve the selected image path
    }

