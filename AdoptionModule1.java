public class Dog {
    private String name, breed, birthday, contact, about;
    private int ageMonths, ageHumanYears;

    public Dog(String name, String breed, int ageMonths, int ageHumanYears,
               String birthday, String contact, String about) {
        this.name = name;
        this.breed = breed;
        this.ageMonths = ageMonths;
        this.ageHumanYears = ageHumanYears;
        this.birthday = birthday;
        this.contact = contact;
        this.about = about;
    }

    // Getters
    public String getName() { return name; }
    public String getBreed() { return breed; }
    public int getAgeMonths() { return ageMonths; }
    public int getAgeHumanYears() { return ageHumanYears; }
    public String getBirthday() { return birthday; }
    public String getContact() { return contact; }
    public String getAbout() { return about; }
}
